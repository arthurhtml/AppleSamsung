                            En effet suite à l’entrée en vigueur du CCPA en Californie (l’équivalent local du RGPD), Samsung vient de mettre à jour sa politique de confidentialité. On découvre alors que le géant coréen collecte et vend les données des smartphones Galaxy depuis des années.
                            Les membres du forum XDA Developers se sont aperçu que Samsung a créé un système complexe pour collecter différents types de données sur les utilisateurs de ses téléphones.
                            Ces différents types de données ont par ailleurs pu être combinés pour créer un profil complet sur l’utilisateur à des fins publicitaires principalement.
                            Samsung précise aussi avoir éventuellement vendu des informations personnelles à des tiers à des fins de business.
