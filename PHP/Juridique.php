                Le RGPD est un règlement qui encadre le traitement des données personnelles dans les 28 pays membres de l’Union Européenne. Il concerne toutes les données des européens collectées par les acteurs du monde entier. Son principal objectif est de mieux protéger ces données personnelles et d’éviter les collectes non désirées.
