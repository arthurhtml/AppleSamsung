                            En mai 2018, à quelques jours de l’entrée en vigueur du RGPD, Apple a mis en ligne une nouvelle section sur son site officiel.
                            Vous y trouverez tous les outils qui mettent le constructeur en conformité avec ce règlement européen et qui permettent à ses utilisateurs d’accéder, de modifier et de supprimer leurs données personnelles.
                            Tous les utilisateurs peuvent supprimer leur compte et obtenir des informations pour corriger leurs données.
                            Les utilisateurs européens bénéficient en outre de deux options supplémentaires : désactiver un compte sans le supprimer et surtout obtenir une copie de toutes les données privées stockées par Apple.
